CREATE TABLE EMP
       (EMPNO NUMBER PRIMARY KEY,
        ENAME VARCHAR2(64),
        JOB VARCHAR2(64),
        MGR NUMBER,
        DEPTNO NUMBER,
        CONSTRAINT EMP_MGR_FK FOREIGN KEY (MGR) REFERENCES EMP(EMPNO),
        CONSTRAINT EMP_DEPT_FK FOREIGN KEY (DEPTNO) REFERENCES DEPT(DEPTNO));

